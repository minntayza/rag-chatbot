-- 3. Chat History Table ────────────────────────────────────────────────────
-- Each row is one message (user or assistant) in a conversation.
-- `session_id` groups messages; generated by the frontend (UUID string).
CREATE TABLE IF NOT EXISTS chat_history (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    session_id  TEXT        NOT NULL,
    role        TEXT        NOT NULL CHECK (role IN ('user', 'assistant')),
    message     TEXT        NOT NULL,
    timestamp   TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Composite index for retrieving a session's history in chronological order.
-- This is the most-used query: "SELECT * WHERE session_id = ? ORDER BY timestamp"
CREATE INDEX IF NOT EXISTS ix_chat_history_session_ts
    ON chat_history (session_id, timestamp);

-- Index for counting messages per session (useful for dashboard analytics)
CREATE INDEX IF NOT EXISTS ix_chat_history_session_id
    ON chat_history (session_id);

-- Index on timestamp for global feeds or admin views
CREATE INDEX IF NOT EXISTS ix_chat_history_ts
    ON chat_history (timestamp DESC);


-- 4. Feedback Table ────────────────────────────────────────────────────────
-- User ratings (thumbs up = +1.0, thumbs down = -1.0) on assistant messages.
-- CASCADE delete: if a chat message is deleted, its feedback is removed too.
CREATE TABLE IF NOT EXISTS feedback (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    message_id  UUID        NOT NULL REFERENCES chat_history(id) ON DELETE CASCADE,
    rating      FLOAT       NOT NULL CHECK (rating >= -1.0 AND rating <= 1.0),
    comment     TEXT,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Index for looking up feedback by message (the most common query)
CREATE INDEX IF NOT EXISTS ix_feedback_message_id
    ON feedback (message_id);

-- Index for rolling up ratings (e.g., average rating per message)
CREATE INDEX IF NOT EXISTS ix_feedback_rating
    ON feedback (rating);

-- Index for time-based analytics (feedback over time)
CREATE INDEX IF NOT EXISTS ix_feedback_created_at
    ON feedback (created_at DESC);


