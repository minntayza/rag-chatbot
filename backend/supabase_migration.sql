-- 1. Enable Extensions ─────────────────────────────────────────────────────
-- pgvector adds the `vector` type and distance operators (<=>, <#>, <+>)
-- pgcrypto enables gen_random_uuid() for default UUID generation
CREATE EXTENSION IF NOT EXISTS vector WITH SCHEMA extensions;
CREATE EXTENSION IF NOT EXISTS pgcrypto WITH SCHEMA extensions;


-- 2. Documents Table ───────────────────────────────────────────────────────
-- Stores one row per text chunk extracted from uploaded PDFs/TXTs.
-- The `embedding` column uses the vector type for cosine similarity search.
-- metadata is a JSONB column for flexible key-value storage (source, page, etc.)
CREATE TABLE IF NOT EXISTS documents (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    filename    TEXT        NOT NULL,
    chunk_index INTEGER     NOT NULL DEFAULT 0,
    content     TEXT        NOT NULL,
    embedding   vector(384)  NOT NULL,   -- all-MiniLM-L6-v2 outputs 384-dim vectors
    metadata    JSONB       DEFAULT '{}'::jsonb,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Index on filename for quick listing of chunks by document
CREATE INDEX IF NOT EXISTS ix_documents_filename
    ON documents (filename);

-- Composite index for retrieving chunks of a file in order
CREATE INDEX IF NOT EXISTS ix_documents_filename_chunk
    ON documents (filename, chunk_index);

-- Index on created_at for filtering recent uploads
CREATE INDEX IF NOT EXISTS ix_documents_created_at
    ON documents (created_at DESC);

