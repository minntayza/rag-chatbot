-- 7. Storage Policies ──────────────────────────────────────────────────────
-- Define who can read/write files in the 'documents' bucket.
-- The backend uploads via service_role key → bypasses RLS entirely.
-- These policies are for direct client-side access if needed later.

-- Allow reading by anyone (for serving document previews)
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'storage_documents_select' AND schemaname = 'storage') THEN
        CREATE POLICY "storage_documents_select" ON storage.objects FOR SELECT USING (bucket_id = 'documents');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'storage_documents_insert' AND schemaname = 'storage') THEN
        CREATE POLICY "storage_documents_insert" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'documents');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'storage_documents_delete' AND schemaname = 'storage') THEN
        CREATE POLICY "storage_documents_delete" ON storage.objects FOR DELETE USING (bucket_id = 'documents');
    END IF;
END $$;


-- 8. match_documents() — pgvector Cosine Similarity Search ─────────────────
-- This function is the heart of the RAG pipeline.
-- Given a 384-dim embedding vector, it returns the top-K most similar chunks
-- using cosine distance (<=> operator).
--
-- Cosine similarity = 1 - cosine_distance
-- Lower distance = more similar     → ORDER BY ... <=> ... ASC
--
-- Called from backend via: supabase.rpc('match_documents', {...})
CREATE OR REPLACE FUNCTION match_documents(
    query_embedding vector(384),
    match_count     INT DEFAULT 5,
    similarity_threshold FLOAT DEFAULT 0.0
)
RETURNS TABLE (
    id          UUID,
    filename    TEXT,
    chunk_index INT,
    content     TEXT,
    similarity  FLOAT,
    metadata    JSONB
)
LANGUAGE plpgsql
STABLE
PARALLEL SAFE
AS $$
BEGIN
    RETURN QUERY
    SELECT
        d.id,
        d.filename,
        d.chunk_index,
        d.content,
        1 - (d.embedding <=> query_embedding) AS similarity,
        d.metadata
    FROM documents d
    WHERE 1 - (d.embedding <=> query_embedding) >= similarity_threshold
    ORDER BY d.embedding <=> query_embedding
    LIMIT match_count;
END;
$$;

-- The function is STABLE (same input → same output within a transaction)
-- and PARALLEL SAFE so pgvector can parallelize the scan across indexes.


-- 9. Helper: delete_all_documents() ────────────────────────────────────────
-- Convenience function to wipe all chunks (useful during development).
CREATE OR REPLACE FUNCTION delete_all_documents()
RETURNS TEXT
LANGUAGE plpgsql
AS $$
DECLARE
    deleted_count INT;
BEGIN
    DELETE FROM documents
    WHERE id IS NOT NULL
    RETURNING count(*) INTO deleted_count;

    RETURN format('Deleted %s document chunks.', deleted_count);
END;
$$;


-- 10. Helper: get_session_stats() ──────────────────────────────────────────
-- Dashboard query: session count, message count, avg messages per session.
CREATE OR REPLACE FUNCTION get_session_stats()
RETURNS TABLE (
    total_sessions  BIGINT,
    total_messages  BIGINT,
    avg_msg_per_session NUMERIC,
    total_documents BIGINT,
    total_feedback  BIGINT,
    avg_rating      NUMERIC
)
LANGUAGE plpgsql
STABLE
PARALLEL SAFE
AS $$
BEGIN
    RETURN QUERY
    SELECT
        (SELECT count(DISTINCT session_id) FROM chat_history) AS total_sessions,
        (SELECT count(*) FROM chat_history) AS total_messages,
        (SELECT ROUND(count(*)::NUMERIC / NULLIF(count(DISTINCT session_id), 0), 1)
         FROM chat_history) AS avg_msg_per_session,
        (SELECT count(DISTINCT filename) FROM documents) AS total_documents,
        (SELECT count(*) FROM feedback) AS total_feedback,
        (SELECT ROUND(avg(rating)::NUMERIC, 2) FROM feedback) AS avg_rating;
END;
$$;


